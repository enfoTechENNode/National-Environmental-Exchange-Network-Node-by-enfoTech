public class DedlPropertyBean {
	private String propertyElementID = "";
	private String propertyName = "";
	private String propertyValue = "";
	/**
	 * @return the propertyElementID
	 */
	public String getPropertyElementID() {
		return propertyElementID;
	}
	/**
	 * @param propertyElementID the propertyElementID to set
	 */
	public void setPropertyElementID(String propertyElementID) {
		this.propertyElementID = propertyElementID;
	}
	/**
	 * @return the propertyName
	 */
	public String getPropertyName() {
		return propertyName;
	}
	/**
	 * @param propertyName the propertyName to set
	 */
	public void setPropertyName(String propertyName) {
		this.propertyName = propertyName;
	}
	/**
	 * @return the propertyValue
	 */
	public String getPropertyValue() {
		return propertyValue;
	}
	/**
	 * @param propertyValue the propertyValue to set
	 */
	public void setPropertyValue(String propertyValue) {
		this.propertyValue = propertyValue;
	}
	
}
